class Osoba{
    private int numerID;
    private int waga;

    public Osoba(int numerID, int waga){
        this.numerID = numerID;
        this.waga = waga;
    }

    public int getNumerID() {
        return numerID;
    }

    public int getWaga() {
        return waga;
    }

    public void setNumerID(int numerID) {
        this.numerID = numerID;
    }

    public void setWaga(int waga) {
        this.waga = waga;
    }
}
